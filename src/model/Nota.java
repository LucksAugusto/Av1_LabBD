package model;

public class Nota {

	private String nomeEscola;
	private String nomeJurado;
	private String nomeQuesito;
	private float nota;
	
	public String getNomeEscola() {
		return nomeEscola;
	}
	public void setNomeEscola(String nomeEscola) {
		this.nomeEscola = nomeEscola;
	}
	public String getNomeJurado() {
		return nomeJurado;
	}
	public void setNomeJurado(String nomeJurado) {
		this.nomeJurado = nomeJurado;
	}
	public String getNomeQuesito() {
		return nomeQuesito;
	}
	public void setNomeQuesito(String nomeQuesito) {
		this.nomeQuesito = nomeQuesito;
	}
	public float getNota() {
		return nota;
	}
	public void setNota(float nota) {
		this.nota = nota;
	}
	
}
